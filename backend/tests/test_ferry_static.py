async def test_fresh_cache_parsed_without_downloading(ferry_paths, monkeypatch):
    write_ferry_zip(ferry_paths)

    async def fail():
        raise AssertionError("should not download with a fresh cache")

    monkeypatch.setattr(ferry_static, "_download_zip", fail)
    data = await ferry_static.load_ferry_static()
    assert data["stops"]["18"]["name"] == "Wall St/Pier 11"
    assert data["trips"]["t1"]["route_id"] == "ER"


async def test_stale_cache_with_failed_download_falls_back(ferry_paths):
    write_ferry_zip(ferry_paths)
    age_file(ferry_paths, days=40)  # past MAX_AGE_DAYS; the dead URL fails fast
    data = await ferry_static.load_ferry_static()
    assert data and "18" in data["stops"]  # stale cache still serves


async def test_missing_cache_with_failed_download_returns_empty(ferry_paths):
    # Lenient failure: no cache and a dead URL yield an EMPTY result, never an
    # exception (the warmup owns retrying).
    assert not ferry_paths.exists()
    data = await ferry_static.load_ferry_static()
    assert data == {}


@pytest.mark.parametrize(
    "make_bad_cache",
    [
        lambda path: path.write_bytes(b"this is not a zip archive"),
        lambda path: write_ferry_zip(path, members={"agency.txt": "agency_id\nNYCF\n"}),
    ],
    ids=["corrupt-zip", "zip-missing-stops.txt"],
)
async def test_unusable_fresh_cache_redownloads_exactly_once(
    ferry_paths, monkeypatch, make_bad_cache
):
    make_bad_cache(ferry_paths)  # fresh mtime; only the recovery download should fire
    calls = []

    async def fake_download():
        calls.append(True)
        write_ferry_zip(ferry_paths)

    monkeypatch.setattr(ferry_static, "_download_zip", fake_download)
    data = await ferry_static.load_ferry_static()
    assert calls == [True]  # exactly one re-download
    assert "18" in data["stops"]


async def test_fresh_but_empty_cache_redownloads_and_recovers(ferry_paths, monkeypatch):
    # A fresh cache that parses to ZERO stops is treated as unusable, so the
    # loader re-downloads once instead of trusting the fresh mtime and wedging
    # until MAX_AGE_DAYS (the empty-valid-cache guard 13a's review hardened).
    write_ferry_zip(ferry_paths, stops=[])  # fresh, valid zip, no stops
    calls = []

    async def fake_download():
        calls.append(True)
        write_ferry_zip(ferry_paths)  # the corrected feed

    monkeypatch.setattr(ferry_static, "_download_zip", fake_download)
    data = await ferry_static.load_ferry_static()
    assert calls == [True]
    assert "18" in data["stops"]


# ---------------- redirect-following download (the ferry-specific quirk) ----------------


class _RedirectHandler(http.server.BaseHTTPRequestHandler):
    """Serve a 302 from /gtfs.aspx to /gtfs.zip, and the zip bytes at /gtfs.zip:
    the exact shape the real Connexionz utility URL exhibits, so the test proves
    the loader follows the redirect (httpx would NOT by default)."""

    def do_GET(self):  # noqa: N802 (BaseHTTPRequestHandler API)
        if self.path.endswith("gtfs.aspx"):
            self.send_response(302)
            self.send_header("Location", "/gtfs.zip")
            self.end_headers()
        elif self.path.endswith("gtfs.zip"):
            body = _zip_bytes()
            self.send_response(200)
            self.send_header("Content-Type", "application/zip")
            self.send_header("Content-Length", str(len(body)))
            self.end_headers()
            self.wfile.write(body)
        else:
            self.send_response(404)
            self.end_headers()

    def log_message(self, *args):  # silence the test server
        pass


async def test_download_follows_the_302_to_the_resource_zip(ferry_paths, monkeypatch):
    server = http.server.HTTPServer(("127.0.0.1", 0), _RedirectHandler)
    thread = threading.Thread(target=server.serve_forever, daemon=True)
    thread.start()
    try:
        host, port = server.server_address
        monkeypatch.setattr(ferry_static, "FERRY_STATIC_URL", f"http://{host}:{port}/gtfs.aspx")
        # No cache exists, so the loader must download by following the redirect;
        # a non-following client would raise_for_status on the 302's empty body.
        data = await ferry_static.load_ferry_static()
    finally:
        server.shutdown()
        thread.join(timeout=5)
    assert data["stops"]["18"]["name"] == "Wall St/Pier 11"
    assert data["trips"]["t1"]["route_id"] == "ER"



# ---------------- build_ferry_route_shapes: modal selection ----------------


def _trip(route, direction, shape):
    return {"route_id": route, "direction_id": direction, "shape_id": shape, "headsign": None}


def test_route_shapes_picks_modal_shape_per_direction():
    # ER dir 0 has shapes a (2 trips) and b (1 trip); modal is a.
    trips = {
        "1": _trip("ER", "0", "a"),
        "2": _trip("ER", "0", "a"),
        "3": _trip("ER", "0", "b"),
    }
    shapes = {"a": [[0, 0], [1, 1], [2, 2]], "b": [[9, 9], [8, 8]]}
    routes = {
        "ER": {
            "long_name": "East River",
            "short_name": None,
            "color": "00839C",
            "text_color": "FFF",
        }
    }
    built = ferry_static.build_ferry_route_shapes(trips, shapes, routes)
    assert len(built) == 1
    entry = built[0]
    assert entry["id"] == "ER" and entry["name"] == "East River" and entry["color"] == "00839C"
    assert entry["shape"] == [shapes["a"]]  # modal shape a, not the minority b


def test_route_shapes_tie_breaks_to_smallest_shape_id():
    trips = {"1": _trip("AS", "0", "z"), "2": _trip("AS", "0", "a")}  # 1 each: tie
    shapes = {"z": [[0, 0], [1, 1]], "a": [[5, 5], [6, 6]]}
    built = ferry_static.build_ferry_route_shapes(trips, shapes, {})
    assert built[0]["shape"] == [shapes["a"]]  # smallest shape_id wins the tie


def test_route_shapes_dedups_reverse_direction_keeps_divergent():
    # dir 0 and dir 1 share the same point set (reverse order): collapse to one.
    fwd = [[0, 0], [1, 1], [2, 2]]
    rev = [[2, 2], [1, 1], [0, 0]]
    trips = {"1": _trip("ER", "0", "f"), "2": _trip("ER", "1", "r")}
    built = ferry_static.build_ferry_route_shapes(trips, {"f": fwd, "r": rev}, {})
    assert len(built[0]["shape"]) == 1  # reverse collapses

    # A genuinely divergent second direction survives.
    other = [[10, 10], [11, 11], [12, 12]]
    trips2 = {"1": _trip("SB", "0", "f"), "2": _trip("SB", "1", "o")}
    built2 = ferry_static.build_ferry_route_shapes(trips2, {"f": fwd, "o": other}, {})
    assert len(built2[0]["shape"]) == 2


def test_route_shapes_drops_route_with_only_degenerate_shapes():
    trips = {"1": _trip("GI", "0", "pt")}
    built = ferry_static.build_ferry_route_shapes(trips, {"pt": [[0, 0]]}, {})  # single point
    assert built == []



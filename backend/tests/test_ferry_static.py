@golden
def test_golden_modal_variant_spread_is_exercised():
    # Five routes carry multiple shape variants in some (route, direction), the
    # deepest being East River with 4 (verified live). The trim keeps every
    # variant, so the fixture still carries the spread that makes modal
    # selection matter.
    trips = ferry_static._parse_trips(_fixture("trips.txt"))

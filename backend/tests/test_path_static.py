def csv_stream(columns, rows) -> io.BytesIO:
    """Render rows as a binary CSV stream, the same shape ZipFile.open yields."""
    buf = io.StringIO()
    writer = csv.DictWriter(buf, fieldnames=columns)
    writer.writeheader()
    for row in rows:
        writer.writerow({c: row.get(c, "") for c in columns})
    return io.BytesIO(buf.getvalue().encode("utf-8"))


def write_path_zip(
    path,
    stops=STOP_ROWS,
    trips=TRIP_ROWS,
    shapes=DEFAULT_SHAPE_ROWS,
    routes=ROUTE_ROWS,
    members=None,
):
    """Write a minimal PATH GTFS zip; `members` overrides the file map entirely."""
    if members is None:
        members = {
            "stops.txt": csv_stream(STOPS_COLS, stops).getvalue(),
            "trips.txt": csv_stream(TRIPS_COLS, trips).getvalue(),
            "shapes.txt": csv_stream(SHAPES_COLS, shapes).getvalue(),
            "routes.txt": csv_stream(ROUTES_COLS, routes).getvalue(),
        }
    with zipfile.ZipFile(path, "w") as zf:
        for name, content in members.items():
            zf.writestr(name, content)



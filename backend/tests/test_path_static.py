@golden
def test_golden_route_shapes_are_the_modal_picks():
    trips = path_static._parse_trips(_fixture("trips.txt"))
    shapes = path_static._parse_shapes(_fixture("shapes.txt"))
    routes_table = path_static._parse_routes(_fixture("routes.txt"))
    built = path_static.build_path_route_shapes(trips, shapes, routes_table)

    # All 7 routes carry usable geometry (verified per route on the live feed).
    assert [r["id"] for r in built] == sorted(EXPECTED_ROUTE_COLORS)
    for entry in built:
        assert entry["color"] == EXPECTED_ROUTE_COLORS[entry["id"]]
        assert entry["name"]

    # Recompute the modal picks independently (most trips per (route,
    # direction), ties to the smallest shape_id) and require every served
    # polyline to BE one of its route's modal shapes: the variant short-turn /
    # track-work shapes must never leak into the output.
    counts = defaultdict(lambda: defaultdict(Counter))
    for t in trips.values():
        if t["route_id"] and t["shape_id"]:
            counts[t["route_id"]][t["direction_id"]][t["shape_id"]] += 1
    for entry in built:
        modal_polylines = []
        for tally in counts[entry["id"]].values():
            shape_id = min(tally, key=lambda s: (-tally[s], s))
            modal_polylines.append(shapes[shape_id])
        for polyline in entry["shape"]:
            assert polyline in modal_polylines



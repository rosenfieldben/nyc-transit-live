# ---------------- build_path_route_shapes (synthetic parsed tables) ----------------


def _trip(route_id, shape_id, direction_id="0"):
    return {"route_id": route_id, "shape_id": shape_id, "direction_id": direction_id}


LINE = [[40.70, -74.00], [40.71, -74.01], [40.72, -74.02], [40.73, -74.03]]
SHORT_TURN = LINE[:2]


def test_modal_shape_wins_by_trip_count():
    # Three trips run the full line, one runs a short-turn variant: the modal
    # full line is the route's shape, the variant is dropped (not deduped in).
    shapes = {"full": LINE, "short": SHORT_TURN}
    trips = {
        "a": _trip("862", "full"),
        "b": _trip("862", "full"),
        "c": _trip("862", "full"),
        "d": _trip("862", "short"),
    }
    routes = path_static.build_path_route_shapes(trips, shapes)
    assert len(routes) == 1
    assert routes[0]["id"] == "862"
    assert routes[0]["shape"] == [LINE]


def test_modal_tie_breaks_to_smallest_shape_id():
    # Equal trip counts: the pick must be deterministic across restarts, so the
    # smallest shape_id wins (set/dict iteration order must not decide).
    branch_a = [[41.0, -74.0], [41.1, -74.1], [41.2, -74.2]]
    branch_z = [[42.0, -74.0], [42.1, -74.1], [42.2, -74.2]]
    shapes = {"a_shape": branch_a, "z_shape": branch_z}
    trips = {"t1": _trip("861", "z_shape"), "t2": _trip("861", "a_shape")}
    routes = path_static.build_path_route_shapes(trips, shapes)
    assert routes[0]["shape"] == [branch_a]


def test_modal_selected_per_direction_and_reverse_collapses():
    # Direction 1's modal is the same line reversed: the point-set dedup reads
    # it as 0% new and collapses it, leaving one rider-facing polyline.
    shapes = {"fwd": LINE, "rev": list(reversed(LINE))}
    trips = {
        "t1": _trip("860", "fwd", "0"),
        "t2": _trip("860", "fwd", "0"),
        "t3": _trip("860", "rev", "1"),
        "t4": _trip("860", "rev", "1"),
    }
    routes = path_static.build_path_route_shapes(trips, shapes)
    assert routes[0]["shape"] == [LINE]


def test_modal_divergent_direction_survives():
    # A direction whose modal genuinely diverges (a different alignment, not a
    # reversal) adds geometry and is kept alongside the other direction's line.
    other = [[45.0, -74.0], [45.1, -74.1], [45.2, -74.2], [45.3, -74.3], [45.4, -74.4]]
    shapes = {"fwd": LINE, "diverged": other}
    trips = {"t1": _trip("1024", "fwd", "0"), "t2": _trip("1024", "diverged", "1")}
    routes = path_static.build_path_route_shapes(trips, shapes)
    # Longest first (the dedup keeps the fullest line first).
    assert routes[0]["shape"] == [other, LINE]


def test_modal_counts_do_not_leak_across_directions():
    # Direction 0 runs the short variant twice, direction 1 runs the full line
    # once. If counts pooled across directions the short would win overall; per
    # direction each is modal in its own group, and the dedup then keeps the
    # full line plus nothing new from the short (a strict subset).
    shapes = {"full": LINE, "short": SHORT_TURN}
    trips = {
        "t1": _trip("859", "short", "0"),
        "t2": _trip("859", "short", "0"),
        "t3": _trip("859", "full", "1"),
    }
    routes = path_static.build_path_route_shapes(trips, shapes)
    assert routes[0]["shape"] == [LINE]  # short is a subset: collapses into full


def test_blank_direction_is_its_own_group():
    shapes = {"full": LINE}
    trips = {"t1": {"route_id": "859", "shape_id": "full", "direction_id": None}}
    routes = path_static.build_path_route_shapes(trips, shapes)

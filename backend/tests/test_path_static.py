# ---------------- load_path_static: download/cache behavior ----------------


async def test_fresh_cache_parsed_without_downloading(path_paths, monkeypatch):
    write_path_zip(path_paths)

    async def fail():  # any download attempt is a test failure
        raise AssertionError("should not download with a fresh cache")

    monkeypatch.setattr(path_static, "_download_zip", fail)
    data = await path_static.load_path_static()
    assert data["stops"]["26733"]["name"] == "Newark"
    assert data["child_to_parent"]["781750"] == "26734"


async def test_stale_cache_with_failed_download_falls_back(path_paths):
    write_path_zip(path_paths)
    age_file(path_paths, days=40)  # past MAX_AGE_DAYS; the dead URL fails fast
    data = await path_static.load_path_static()
    assert data and "26733" in data["stops"]


async def test_missing_cache_with_failed_download_returns_empty(path_paths):
    # Lenient failure, matching load_railroad_static: no cache and a dead URL
    # yield an EMPTY result, never an exception (the warmup owns retrying).
    assert not path_paths.exists()
    data = await path_static.load_path_static()
    assert data == {}


@pytest.mark.parametrize(
    "make_bad_cache",
    [
        lambda path: path.write_bytes(b"this is not a zip archive"),
        lambda path: write_path_zip(path, members={"agency.txt": "agency_id\nPATH\n"}),
    ],
    ids=["corrupt-zip", "zip-missing-stops.txt"],
)
async def test_unusable_fresh_cache_redownloads_exactly_once(
    path_paths, monkeypatch, make_bad_cache
):
    make_bad_cache(path_paths)  # fresh mtime; only the recovery download should fire
    calls = []

    async def fake_download():
        calls.append(True)
        write_path_zip(path_paths)

    monkeypatch.setattr(path_static, "_download_zip", fake_download)
    data = await path_static.load_path_static()
    assert calls == [True]  # exactly one re-download
    assert "26733" in data["stops"]


async def test_unusable_cache_and_failed_redownload_returns_empty(path_paths):
    path_paths.write_bytes(b"this is not a zip archive")  # fresh mtime, dead URL
    data = await path_static.load_path_static()
    assert data == {}


# A structurally valid zip whose stops.txt has NO parent stations (only child
# platforms): _parse_zip succeeds but data["stops"] is empty. This is the
# "fresh-but-empty cache" the loader must not wedge on.
EMPTY_PARENT_STOPS = [
    {
        "stop_id": "781718",
        "stop_name": "Newark Track 1",
        "stop_lat": "40.73454",
        "stop_lon": "-74.16375",
        "location_type": "0",
        "parent_station": "26733",
    }
]


async def test_fresh_but_empty_cache_redownloads_and_recovers(path_paths, monkeypatch):
    # A fresh cache that parses to ZERO parent stations is treated as unusable,
    # so the loader re-downloads once instead of trusting the fresh mtime and
    # wedging until MAX_AGE_DAYS. Without this a transiently-empty upstream would
    # never be refetched even after it self-corrected.
    write_path_zip(path_paths, stops=EMPTY_PARENT_STOPS)  # fresh, valid, no parents
    calls = []

    async def fake_download():
        calls.append(True)
        write_path_zip(path_paths)  # upstream corrected: now has parent stations

    monkeypatch.setattr(path_static, "_download_zip", fake_download)
    data = await path_static.load_path_static()
    assert calls == [True]  # exactly one recovery download
    assert "26733" in data["stops"]  # the corrected feed is picked up


async def test_persistently_empty_feed_returns_empty(path_paths):
    # Fresh empty cache + a dead URL: the recovery re-download also fails, so the
    # result is {} and the warmup marks the group failed (single-system PATH). The
    # cache invalidation still fired, so the NEXT warm retry re-downloads again.
    write_path_zip(path_paths, stops=EMPTY_PARENT_STOPS)  # fresh, valid, no parents
    data = await path_static.load_path_static()
    assert data == {}



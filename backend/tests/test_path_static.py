def _trip(route_id, shape_id, direction_id="0"):
    return {"route_id": route_id, "shape_id": shape_id, "direction_id": direction_id}


LINE = [[40.70, -74.00], [40.71, -74.01], [40.72, -74.02], [40.73, -74.03]]
SHORT_TURN = LINE[:2]


def test_modal_shape_wins_by_trip_count():
    # Three trips run the full line, one runs a short-turn variant: the modal
    # full line is the route's shape, the variant is dropped (not deduped in).
    shapes = {"full": LINE, "short": SHORT_TURN}
    trips = {
        "a": _trip("862", "full"),
        "b": _trip("862", "full"),
        "c": _trip("862", "full"),
        "d": _trip("862", "short"),
    }
    routes = path_static.build_path_route_shapes(trips, shapes)
    assert len(routes) == 1
    assert routes[0]["id"] == "862"
    assert routes[0]["shape"] == [LINE]


def test_modal_tie_breaks_to_smallest_shape_id():
    # Equal trip counts: the pick must be deterministic across restarts, so the
    # smallest shape_id wins (set/dict iteration order must not decide).
    branch_a = [[41.0, -74.0], [41.1, -74.1], [41.2, -74.2]]
    branch_z = [[42.0, -74.0], [42.1, -74.1], [42.2, -74.2]]
